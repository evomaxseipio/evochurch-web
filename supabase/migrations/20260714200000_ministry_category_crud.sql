CREATE TABLE IF NOT EXISTS public.ministry_category (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  church_id integer NOT NULL REFERENCES public.church (id) ON DELETE CASCADE,
  code text NOT NULL,
  name text NOT NULL,
  description text NOT NULL DEFAULT '',
  sort_order integer NOT NULL DEFAULT 100,
  is_active boolean NOT NULL DEFAULT true,
  is_system boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT ministry_category_church_code_unique UNIQUE (church_id, code),
  CONSTRAINT ministry_category_code_format CHECK (code ~ '^[a-z][a-z0-9_]*$'),
  CONSTRAINT ministry_category_name_not_blank CHECK (length(trim(name)) > 0)
);

COMMENT ON TABLE public.ministry_category IS
  'Catálogo de categorías de ministerio por iglesia. Codes semánticos alimentan filtros y presets de asistencia.';

CREATE INDEX IF NOT EXISTS idx_ministry_category_church_active
  ON public.ministry_category (church_id, is_active, sort_order);

