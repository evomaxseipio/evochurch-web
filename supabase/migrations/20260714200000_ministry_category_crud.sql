CREATE OR REPLACE FUNCTION public.fn_seed_ministry_categories(p_church_id integer)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO public
AS $$
BEGIN
  INSERT INTO public.ministry_category (
    church_id, code, name, description, sort_order, is_active, is_system
  )
  VALUES
    (p_church_id, 'discipleship', 'Discipulado',
     'Estudios bíblicos, escuela dominical y grupos de enseñanza.', 10, true, true),
    (p_church_id, 'house_group', 'Casas fuente',
     'Ministerios de casas / grupos en hogar.', 20, true, true),
    (p_church_id, 'cell_group', 'Célula',
     'Células o grupos pequeños de crecimiento.', 30, true, true),
    (p_church_id, 'children', 'Niños',
     'Ministerio infantil y escuela de niños.', 40, true, true),
    (p_church_id, 'worship', 'Adoración / culto',
     'Alabanza, culto y equipos de adoración.', 50, true, true),
    (p_church_id, 'other', 'Otro',
     'Categoría general u otros ministerios.', 90, true, true)
  ON CONFLICT (church_id, code) DO NOTHING;
END;
$$;

CREATE OR REPLACE FUNCTION public.trg_fn_seed_ministry_categories()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO public
AS $$
BEGIN
  PERFORM public.fn_seed_ministry_categories(NEW.id);
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_church_seed_ministry_categories ON public.church;
CREATE TRIGGER trg_church_seed_ministry_categories
  AFTER INSERT ON public.church
  FOR EACH ROW
  EXECUTE FUNCTION public.trg_fn_seed_ministry_categories();

-- Seed existing churches
DO $$
DECLARE
  r record;
BEGIN
  FOR r IN SELECT id FROM public.church LOOP
    PERFORM public.fn_seed_ministry_categories(r.id);
  END LOOP;
END $$;

