UPDATE public.church_ministries
SET ministry_category = 'other'
WHERE ministry_category IS NULL
   OR btrim(ministry_category) = ''
   OR ministry_category NOT IN (
     'discipleship', 'house_group', 'cell_group', 'children', 'worship', 'other'
   );

ALTER TABLE public.church_ministries
  ALTER COLUMN ministry_category SET DEFAULT 'other';

ALTER TABLE public.church_ministries
  ALTER COLUMN ministry_category SET NOT NULL;

-- Orphan custom codes that somehow exist: map to other before FK
UPDATE public.church_ministries cm
SET ministry_category = 'other'
WHERE NOT EXISTS (
  SELECT 1
  FROM public.ministry_category mc
  WHERE mc.church_id = cm.church_id
    AND mc.code = cm.ministry_category
);

ALTER TABLE public.church_ministries
  DROP CONSTRAINT IF EXISTS church_ministries_ministry_category_fk;

ALTER TABLE public.church_ministries
  ADD CONSTRAINT church_ministries_ministry_category_fk
  FOREIGN KEY (church_id, ministry_category)
  REFERENCES public.ministry_category (church_id, code)
  ON UPDATE CASCADE
  ON DELETE RESTRICT;

COMMENT ON COLUMN public.church_ministries.ministry_category IS
  'Code de ministry_category (FK church_id+code). Seeds: discipleship|house_group|cell_group|children|worship|other + custom.';

CREATE INDEX IF NOT EXISTS idx_church_ministries_church_category
  ON public.church_ministries (church_id, ministry_category);
